/**
 * 핵심 요약 한 줄에 어울리는 단일 이모지(플랫폼 기본 렌더링).
 * 문장 키워드 기반 휴리스틱 — LLM 호출 없음.
 */
export function pickSummaryEmoji(line: string): string {
  const t = line.trim();
  if (!t) return "💬";

  const rules: [RegExp, string][] = [
    [/만원|만 원|\d+\s*원|가격|비용|가성비|돈|할부|예산|통장|저축|이자|결합|요금/i, "💸"],
    [/위험|리스크|안전|사기|피해|보증|배터리|수리|고장|직거래|검수/i, "⚠️"],
    [/추천|가자|가면|선택|구매|사러|살 거|새 폰|새거|풀옵|업그레이드/i, "✅"],
    [/안 살|보류|미루|패스|금지|하지 말|안 마셔|안 가|그만|됐어/i, "🙅"],
    [/절충|타협|중간|한 잔만|조건부|둘 다|타협안|실무 조건/i, "⚖️"],
    [/반박|아니|그건|틀렸|잠깐|무리|과해/i, "🤔"],
    [/투표|정리\s*:|결론|최종|MC|한 줄|못 박/i, "🎯"],
    [/맛집|식당|먹|메뉴|저녁|점심|브런치|예약/i, "🍽️"],
    [/술|와인|맥주|한잔|마셔|음주|알코올/i, "🍷"],
    [/이직|회사|직장|연봉|퇴사|야근/i, "💼"],
    [/연애|헤어|썸|남친|여친|이별|결혼/i, "💕"],
    [/시간|일정|내일|오늘 밤|늦게|아침에/i, "⏰"],
    [/경험|써봤|체감|느낌|일상|습관/i, "✨"],
    [/숫자|근거|비교|스펙|성능|렉/i, "📊"],
    [/여행|항공|숙소|호텔|제주/i, "✈️"],
    [/운동|건강|수면|병원|약/i, "🩺"],
  ];

  for (const [re, emoji] of rules) {
    if (re.test(t)) return emoji;
  }
  return "💬";
}
